############################################# DATOS SIMPLES ##########################################

#################### STRING ####################
### Para strings de una sola linea (sin saltos de linea)
String1 = 'Palabra 1'
print(String1)

String2 = "Palabra 2"
print(String2)

### Para strings de varias lineas (con saltos de linea)
String3 = '''Oración  
de varias 
lineas 1'''
print(String3)

String4 = """Oración  
de varias 
lineas 2"""
print(String4)


#################### Numeros ####################
### INT --> Numeros enteros
Entero = 123
print(Entero)

### FLOAT --> Numeros decimales
Decimal = 74.64
print(Decimal)


#################### Boleanos ####################
### Solo puede tomar valores de True o False
Boleano1 = True
print(Boleano1)

Boleano2 = False
print(Boleano2)